package Partida;

	public class Personagem {
	
		private String nomeP;
		private Boolean statusP;
		private int tempoDeVida;
		private int idP;
		private String habilidadeP;
		
			public Personagem() {
		
			}

				public String getNomeP() {
					return nomeP;
				}
			
				public void setNomeP(String nomeP) {
					this.nomeP = nomeP;
				}

				public Boolean getStatusP() {
					return statusP;
				}
			
				public void setStatusP(Boolean statusP) {
					this.statusP = statusP;
				}
			
				public int getTempoDeVida() {
					return tempoDeVida;
				}

				public void setTempoDeVida(int tempoDeVida) {
					this.tempoDeVida = tempoDeVida;
				}
			
				public int getIdP() {
					return idP;
				}
			
				public void setIdP(int idP) {
					this.idP = idP;
				}

				public String getHabilidadeP() {
					return habilidadeP;
				}

				public void setHabilidadeP(String habilidadeP) {
					habilidadeP = habilidadeP;
				}
	
	
}
